package java_project;

public class Jugador {
	private String nombre;
	private String apellidos;
	private int edad;
	private double estatura;
	private double peso;
	private double valoracion;
	private int minJugados;
	private int partidosJugados;

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre
	 *            the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the apellidos
	 */
	public String getApellidos() {
		return apellidos;
	}

	/**
	 * @param apellidos
	 *            the apellidos to set
	 */
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}

	/**
	 * @return the edad
	 */
	public int getEdad() {
		return edad;
	}

	/**
	 * @param edad
	 *            the edad to set
	 */
	public void setEdad(int edad) {
		this.edad = edad;
	}

	/**
	 * @return the estatura
	 */
	public double getEstatura() {
		return estatura;
	}

	/**
	 * @param estatura
	 *            the estatura to set
	 */
	public void setEstatura(double estatura) {
		this.estatura = estatura;
	}

	/**
	 * @return the peso
	 */
	public double getPeso() {
		return peso;
	}

	/**
	 * @param peso
	 *            the peso to set
	 */
	public void setPeso(double peso) {
		this.peso = peso;
	}

	/**
	 * @return the valoracion
	 */
	public double getValoracion() {
		return valoracion;
	}

	/**
	 * @param valoracion
	 *            the valoracion to set
	 */
	public void setValoracion(double valoracion) {
		this.valoracion = valoracion;
	}

	/**
	 * @return the minJugados
	 */
	public int getMinJugados() {
		return minJugados;
	}

	/**
	 * @param minJugados
	 *            the minJugados to set
	 */
	public void setMinJugados(int minJugados) {
		this.minJugados = minJugados;
	}

	/**
	 * @return the partidosJugados
	 */
	public int getPartidosJugados() {
		return partidosJugados;
	}

	/**
	 * @param partidosJugados
	 *            the partidosJugados to set
	 */
	public void setPartidosJugados(int partidosJugados) {
		this.partidosJugados = partidosJugados;
	}

}
