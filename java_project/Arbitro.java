package java_project;

public class Arbitro {
	private String nombre;
	private String apellidos;
	private double mediamarillas;
	private double mediarojas;
	private double mediafaltaspitadas;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the apellidos
	 */
	public String getApellidos() {
		return apellidos;
	}
	/**
	 * @param apellidos the apellidos to set
	 */
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	/**
	 * @return the mediamarillas
	 */
	public double getMediamarillas() {
		return mediamarillas;
	}
	/**
	 * @param mediamarillas the mediamarillas to set
	 */
	public void setMediamarillas(double mediamarillas) {
		this.mediamarillas = mediamarillas;
	}
	/**
	 * @return the mediarojas
	 */
	public double getMediarojas() {
		return mediarojas;
	}
	/**
	 * @param mediarojas the mediarojas to set
	 */
	public void setMediarojas(double mediarojas) {
		this.mediarojas = mediarojas;
	}
	/**
	 * @return the mediafaltaspitadas
	 */
	public double getMediafaltaspitadas() {
		return mediafaltaspitadas;
	}
	/**
	 * @param mediafaltaspitadas the mediafaltaspitadas to set
	 */
	public void setMediafaltaspitadas(double mediafaltaspitadas) {
		this.mediafaltaspitadas = mediafaltaspitadas;
	}
	
	
}
